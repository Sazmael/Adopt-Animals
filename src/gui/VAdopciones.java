    private void jTablaListaTipoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablaListaTipoMouseClicked
        //Pasar un auxiliar el indice de la tabla seleccionada
        indice = jTablaListaTipo.rowAtPoint(evt.getPoint());
        //Auxiliar de ID animal y descripción segun el indice clickeado
        idAnimal = Integer.parseInt(listTipo.getValueAt(indice, 0).toString());
        descripcion = listTipo.getValueAt(indice, 1).toString();

        //Habilitar botones de la pantalla adopción si se marca un tipo
        if(indice >= 0){
            btn_GenerarDNI.setEnabled(true);
            txt_Nombre.setEnabled(true);
            txt_Costo.setEnabled(true);
            txt_Domicilio.setEnabled(true);
            check_CertificacionRaza.setEnabled(true);

        }

    }//GEN-LAST:event_jTablaListaTipoMouseClicked

