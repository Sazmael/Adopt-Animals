    private void btn_GuardarAdopcionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GuardarAdopcionActionPerformed
        double costoAux; //Auxiliar de costo para asignarle dos decimales
        Adopciones adoptar = new Adopciones(); //Instancia de nuevo adoptar

        //Recoger los datos de adoptar
        adoptar.setIdTipoAnimal(idAnimal);
        adoptar.setDescripcion(descripcion);

        adoptar.setDni(this.txt_DNI.getText());
        adoptar.setNombre(this.txt_Nombre.getText());

        if(check_CertificacionRaza.isSelected()){
            adoptar.setCertificacionRaza(true);
        }else{
            adoptar.setCertificacionRaza(false);
        }

        adoptar.setDomicilio(this.txt_Domicilio.getText());

        //Metodo para asignar dos decimales maximo al numero digitado
        DecimalFormat df = new DecimalFormat("#.00");
        costoAux = Double.valueOf(this.txt_Costo.getText());

        adoptar.setCosto(Double.valueOf(df.format(costoAux))); //Pasar el dato al objeto costo

        //Recorrer el stack con un iterator para asignar las colas independientes
        VTipoAnimal tipo = new VTipoAnimal();
        for (Iterator<TipoAnimales> iterator = tipo.tipoStack.iterator(); iterator.hasNext();) {
            TipoAnimales next = iterator.next();

            //Condicional para guardar los datos de adoptar a su respectiva ID en las colas por ID tipo animal
            if(next.getIdTipoAnimal() == idAnimal){

                next.getAdopcionesQueue().offer(adoptar);

            }

        }
        refrescarTablaTipo();
        cargarStackTipos();
        reset();
    }//GEN-LAST:event_btn_GuardarAdopcionActionPerformed

