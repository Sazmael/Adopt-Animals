    public void habilitarbtnGuardar(){
         
         if(!txt_DNI.getText().isEmpty() && !txt_Nombre.getText().isEmpty() 
         && !txt_Costo.getText().isEmpty() && !txt_Domicilio.getText().isEmpty()){
             btn_GuardarAdopcion.setEnabled(true);          
         }else{
             btn_GuardarAdopcion.setEnabled(false);
         }
    
     }
    
    public void refrescarTablaTipo(){
        int fila;
        //Borrar datos de tablas tipo
        fila = listTipo.getRowCount();

        for(int i = fila-1;i >=0; i--){  
         listTipo.removeRow(i);
         
        } 
    }
    
    private void cargarStackTipos(){
         //Recorrer el stack para llenar las tablas tipoAnimal
         for (Iterator<TipoAnimales> iterator = tipo.tipoStack.iterator(); iterator.hasNext();) {
            TipoAnimales next = iterator.next();
            
            listTipo.addRow(new Object[]{

                next.getIdTipoAnimal(), next.getDescripcion()

            });
         }
     }
    
